int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	nb = 1;
	if (i < 0 || i >= 13)
	{
		return (0);
	}
	else if (i > 1)
	{
		while (i > 1)
		{
			nb = nb * i;
			i--;
		}
	}
	else if (i == 0 || i == 1)
	{
		nb = 1;
	}
	return (nb);
}
