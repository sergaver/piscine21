int		ft_is_prime(int nb)
{
	int delitel;

	if (nb <= 1)
	{
		return (0);
	}
	delitel = nb - 1;
	while (1)
	{
		if (delitel == 1)
		{
			return (1);
		}
		if ((nb % delitel) == 0)
		{
			return (0);
		}
		delitel--;
	}
}
