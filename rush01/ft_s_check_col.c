#include "ft.h"

int		ft_s_check_col(char **array, int line, int col)
{
	int i;
	int j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (array[i][col] == array[j][col] && i != j)
				return (0);
		}
	}
	return (1);
}
