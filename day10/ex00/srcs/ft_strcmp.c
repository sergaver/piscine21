int		ft_strcmp(char *s1, char *s2)
{
	int div;
	int i;

	i = 0;
	while ((s1[i] == s2[i]) && (s1[i] != '\0') && (s2[i] != '\0'))
		i++;
	if (s1[i] != s2[i])
	{
		div = s1[i] - s2[i];
		return (div);
	}
	else
		return (0);
}
