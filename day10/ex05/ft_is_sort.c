int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;

	i = 0;
	if (length == 1)
		return (1);
	while (i < length - 1)
	{
		return (f(tab[i], tab[i + 1]) <= 0) ? 1 : 0;
		i++;
	}
	return (0);
}
